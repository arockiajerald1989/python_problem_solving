# Find Remainder, Quotient & True Division of int expression a/b

a = int(input("Enter value of a: "))
b = int(input("Enter value of b: "))
true_division = a/b
quotient = a//b
remainder = a%b
print(f"true_division: {true_division}")
print(f"quotient: {quotient}")
print(f"remainder: {remainder}")